package org.example.chapter6.method;

public class MyMath {

    long add (long a, long b) {
        long result = a + b;
        return result;  // 간단하게 한줄로 return a + b; 로 요약 가능
    }

    long subtract(long a, long b) {
        return a - b;
    }

    long multiply(long a, long b) {
        return a * b;
    }

    double divide(double a, double b) {
        return a / b;
    }


}
